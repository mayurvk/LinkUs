    private void srch_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srch_bttnActionPerformed
        if(check_id()||!srch_name.getText().equals(""))
        {
            try{
                    MongoClient mc;
                    mc = new MongoClient("localhost",27017);
                    DB db = mc.getDB("linkus");
                    DBCollection coll = db.getCollection("patient_details");
                    BasicDBObject obj = new BasicDBObject();
                    if(srch_id.getText().equals(""))
                        obj.put("p_name",srch_name.getText());
                    else
                        obj.put("p_id", srch_id.getText());

                    DBCursor cursor = coll.find(obj);
                    while(cursor.hasNext())
                    {
                        LocalDate today = LocalDate.now();
                        DateTimeFormatter dtf=DateTimeFormatter.ofPattern("dd/MM/yyyy",Locale.ENGLISH);
                        LocalDate birthday = LocalDate.parse((String)cursor.next().get("p_dob"), dtf);
                        Period p = Period.between(birthday, today);
                        srch_id.setText((String)cursor.curr().get("p_id"));
                        p_age_exist.setText(String.valueOf(p.getYears()));
                        p_name_exist.setText((String)cursor.curr().get("p_name"));
                        p_addr_exist.setText((String)cursor.curr().get("p_addr"));
                        p_gender_exist.setText((String)cursor.curr().get("p_gender"));
                        p_phno_exist.setText((String)cursor.curr().get("p_phno"));
                        Absclass.p_id=srch_id.getText();
                    }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this,"Your database server is not responding ...","Error",1);
                System.out.println(e.getMessage());
            };
        }
    }//GEN-LAST:event_srch_bttnActionPerformed

