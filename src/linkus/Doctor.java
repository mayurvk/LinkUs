    public int validation()
    {
        boolean flag=true;
        String pid = p_id_text.getText();
        String pname = p_name_text.getText();
        String phno = p_phno_text.getText();
        String email = p_email_text.getText();
        String city = p_city_text.getText();
        String dob = p_dob_text.getText();
        
        label_reset();
        
        //if all elements are blank
        if(pid.equals("")&&pname.equals("")&&phno.equals("")&&city.equals("")&&dob.equals(""))
           {
                 p_id_label.setText("Please Enter Valid Patient ID");
                 p_name_label.setText("Please Enter Valid Patient Name");
                 p_phno_label.setText("Please Enter Phone Number");
                 p_dob_label.setText("Please Enter Date of Birth");
                 flag=false;
           }
        
        if(pid.equals(""))
        {
                  p_id_label.setText("Please Enter Valid Patient ID");
                  flag=false;
        }
        
        //Patient Name Validation
        if(pname.equals(""))
        {
                 p_name_label.setText("Please Enter Valid Patient Name");
                 flag=false;
        }
        //Phone Number Validation
        if(phno.equals("")){
                p_phno_label.setText("Please Enter Phone Number");
                flag=false;
        }
        else{
                String pn=p_phno_text.getText();
                Pattern pattern2 = Pattern.compile("\\d{10}");
                Matcher matcher2 = pattern2.matcher(pn);
                
                                if (matcher2.matches()==false)
                                {
                                   p_phno_label.setText("Please Enter valid Phone Number");
                                   flag=false;
                                }
        }
        //Date Of Birth Validation
        if(dob.equals(""))
        {
           p_dob_label.setText("Please Enter Date of Birth");
           flag=false;
        }
        else
        {
            Pattern pattern4 =Pattern.compile("([0-9]{2})/([0-9]{2})/([0-9]{4})");
            Matcher matcher4 =pattern4.matcher(p_dob_text.getText());
            if (matcher4.matches()==false)
            {
                p_dob_label.setText("InValid Dob ");
                flag=false;
            }
            
        }
        //Gender Validation
        
        if(flag)
            return 1; 
        else
            return 0;
    }
    
